    private void submitButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitButtonMouseClicked
        // TODO add your handling code here:
        if (isInputValid()) {
            try {
                String m = monthComboBox.getSelectedIndex() + 1 > 9 ? "" : "0";
                m = m + (monthComboBox.getSelectedIndex() + 1);
                DataBase.signUp(lastNameTextField.getText(), firstNameTextField.getText(),
                        passwordField1.getText(), emailTextField.getText(), yearComboBox.getSelectedItem().toString()
                        + "-" + m + "-" + dayComboBox.getSelectedItem().toString(),
                        genderComboBox.getSelectedItem().toString(), cityTextField.getText(), addressTextField.getText());
                new Login().setVisible(true);
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(CreateAccount.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, invalidInput);
        }

    }//GEN-LAST:event_submitButtonMouseClicked

