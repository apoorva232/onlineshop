    private boolean isInputValid() {
        if (firstNameTextField.getText().length() < 1) {
            invalidInput = "Invalid first name";
            return false;
        }
        if (lastNameTextField.getText().length() < 1) {
            invalidInput = "Invalid last name";
            return false;
        }
        if (emailTextField.getText().indexOf("@") < 0) /* || emailTextField.toString().length() < 3 ||
                emailTextField.toString().charAt(emailTextField.toString().length() -1) == '@' || 
                emailTextField.toString().charAt(0) == '@')*/ {
            System.out.println(emailTextField.toString());
            invalidInput = "Invalid email";
            return false;
        }
        if (!passwordField1.getText().equals(passwordField2.getText()) || passwordField1.getText().length() < 6) {
            invalidInput = "Invalid password";
            return false;
        }
        if (cityTextField.getText().length() < 1) {
            invalidInput = "Invalid city";
            return false;
        }
        if (addressTextField.getText().length() < 1) {
            invalidInput = "Invalid address";
            return false;
        }
        if (Integer.parseInt(yearComboBox.getSelectedItem().toString()) > 2005) {
            invalidInput = "Invalid age";
            return false;
        }

        return true;
    }

